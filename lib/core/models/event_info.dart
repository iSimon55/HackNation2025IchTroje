class EventInfo {
  final String id;
  final String title;
  final String date;
  final String location;
  final String description;
  final String emoji;
  final String colorHex;
  final String websiteUrl;

  EventInfo({
    required this.id,
    required this.title,
    required this.date,
    required this.location,
    required this.description,
    required this.emoji,
    required this.colorHex,
    required this.websiteUrl,
  });
}
